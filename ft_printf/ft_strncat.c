#include "ft_printf.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;
	int k;

	k = 0;
	i = ft_strlen(s1);
	while (s2[k] && k < (int)n)
		s1[i++] = s2[k++];
	s1[i] = '\0';
	return (s1);
}
